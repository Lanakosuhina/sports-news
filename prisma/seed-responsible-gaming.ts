import { PrismaClient } from '@prisma/client'

const prisma = new PrismaClient()

async function main() {
  const content = `
<div class="responsible-gaming-content">
  <p class="lead">Азартные игры должны приносить удовольствие и развлечение. Если вы заметили, что ставки начали занимать слишком много времени, отнимают значительную часть вашего бюджета или вызывают стресс — это сигнал сделать паузу.</p>

  <h2>Когда нужно остановиться</h2>
  <p>Рекомендуется снизить активность или полностью прекратить участие в ставках, если вы заметили хотя бы один из следующих признаков:</p>
  <ul>
    <li>вы берёте кредиты или занимаете деньги у друзей и семьи для участия в играх;</li>
    <li>каждый проигрыш вызывает желание немедленно отыграться и вернуть потери;</li>
    <li>вы скрываете информацию о своём участии в ставках от близких людей;</li>
    <li>сумма ставок постоянно растёт, и это негативно влияет на ваш финансовый достаток;</li>
    <li>вы теряете контроль над временем, проводимым в играх;</li>
    <li>ставки мешают работе, учёбе или личным отношениям.</li>
  </ul>

  <h2>Что делать, если вы заметили проблему</h2>
  <p>Помните: это решаемо. Первый и самый важный шаг — честно признать наличие проблемы перед собой. Это требует смелости, но это необходимо.</p>

  <p><strong>Расскажите близким.</strong> Поддержка семьи и друзей — один из ключевых факторов успешного преодоления зависимости. Не стесняйтесь открыться тем, кому вы доверяете.</p>

  <p><strong>Обратитесь к специалисту.</strong> В вашем городе работают квалифицированные психологи и наркологи, имеющие опыт лечения игровой зависимости. Не переживайте — вы точно не первый, кто приходит с такой проблемой. Это нормальная ситуация, и профессионалы знают, как помочь.</p>

  <p><strong>Установите ограничения.</strong> Большинство площадок для ставок предоставляют инструменты самоограничения: лимиты на сумму ставок, ограничения по времени доступа или полную блокировку аккаунта.</p>

  <h2>Ресурсы и поддержка</h2>
  <p>Не стесняйтесь обращаться за помощью. Помимо профессиональных врачей, существуют общественные организации и горячие линии, готовые оказать поддержку на любом этапе.</p>

  <p class="highlight"><strong>Ваше благополучие важнее любой ставки. Сделайте выбор в пользу здоровья и стабильности уже сегодня.</strong></p>
</div>
  `.trim()

  const page = await prisma.page.upsert({
    where: { slug: 'responsible-gaming' },
    update: {
      title: 'Играйте Ответственно',
      content,
      metaTitle: 'Играйте Ответственно | Тренды спорта',
      metaDescription: 'Информация об ответственной игре. Признаки игровой зависимости и ресурсы для получения помощи.',
      isPublished: true,
    },
    create: {
      title: 'Играйте Ответственно',
      slug: 'responsible-gaming',
      content,
      metaTitle: 'Играйте Ответственно | Тренды спорта',
      metaDescription: 'Информация об ответственной игре. Признаки игровой зависимости и ресурсы для получения помощи.',
      isPublished: true,
    },
  })

  console.log('Created/updated responsible-gaming page:', page.id)
}

main()
  .catch((e) => {
    console.error(e)
    process.exit(1)
  })
  .finally(async () => {
    await prisma.$disconnect()
  })
